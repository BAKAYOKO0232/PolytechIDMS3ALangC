/*
-----------------------------------------------------
ROLE : calcule (a^n)
------------------------------------------------------
PRECONDITION : a est un nombre reel et n est un entier >= 0
------------------------------------------------------
POSTCONDITION : a^n = 1 (si n=0) || a^n = a*a*.....*a (n fois)
------------------------------------------------------
*/
double power(double a, int n)
{
    double resultat = 0;
    if (n == 0)
    {
        resultat = 1;
    }else{
        resultat = a*power(a,(n-1));
    }
    return resultat;
}
